#include<iostream>
using namespace std;
int main(){
	for(int b = 1;b <=3;){
		cout<<"lo"<<endl;
		b++;  
	}
	
	for(int b = 1,c = 10;b<=3||c<=5;b++,c--){
		cout<<"le"<<endl;
	}


	cout<<"----------------------------------"<<endl;

	int z = 1;
	for(;;){
		cout<<"lu"<<endl;
		z++;
		if(z==3){
			break;
	}
}
}
